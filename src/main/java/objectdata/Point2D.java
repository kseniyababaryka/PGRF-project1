package objectdata;

public class Point2D {

    private int r1;
    private int c1;

    public Point2D(int c1, int r1){
        this.c1 = c1;
        this.r1 = r1;
    }

    public int getR1(){
        return r1;
    }

    public void setR1(int r1){
        this.r1 = r1;
    }

    public int getC1(){
        return c1;
    }

    public void setC1(int c1){
        this.c1 = c1;
    }


}
